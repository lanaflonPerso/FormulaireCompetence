package fr.dawan.bean;

/**
 * The Class DbObject.
 * Class for persistable Objects.
 * @author Pierre
 */
public abstract class DbObject {
	
	/** The id. */
	private int id;
	
	/** The name. */
	private String name;
	
	
	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * Sets the name.
	 *
	 * @param name the new name
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
